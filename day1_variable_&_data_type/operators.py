#relational operators(relational operators always gives a boolean value)
x = 300
y = 15
print(x == y)#a is equal to b
print(x != y)#a is not equal to b
print(x > y)#a is greater than b
print(x < y)#a is less than b
print(x >= y)#a is greater & equal than b
print(x <= y)#a is less & equal than b



